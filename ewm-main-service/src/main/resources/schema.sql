DROP TABLE IF EXISTS users CASCADE;
DROP TABLE IF EXISTS categories CASCADE;
DROP TABLE IF EXISTS locations CASCADE;
DROP TABLE IF EXISTS events CASCADE;
DROP TABLE IF EXISTS requests CASCADE;
DROP TABLE IF EXISTS compilations CASCADE;
DROP TABLE IF EXISTS compilations_events CASCADE;


CREATE TABLE IF NOT EXISTS users(
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
email VARCHAR(254) NOT NULL,
name VARCHAR(250) NOT NULL,
CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS categories(
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
name VARCHAR(128) NOT NULL,
CONSTRAINT UQ_CATEGORY_NAME UNIQUE (name)
);

CREATE TABLE IF NOT EXISTS locations (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
lat FLOAT NOT NULL,
lon FLOAT NOT NULL
);

CREATE TABLE IF NOT EXISTS events (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
annotation VARCHAR(2000) NOT NULL,
category_id BIGINT NOT NULL,
created_on TIMESTAMP NOT NULL,
description VARCHAR(7000) NOT NULL,
event_date TIMESTAMP NOT NULL,
initiator_id BIGINT NOT NULL,
location_id BIGINT NOT NULL,
paid BOOLEAN NOT NULL,
participant_limit BIGINT  DEFAULT 0,
published_on TIMESTAMP,
request_moderation BOOLEAN DEFAULT TRUE,
state VARCHAR NOT NULL,
title VARCHAR(120) NOT NULL,
CONSTRAINT fk_category FOREIGN KEY (category_id) REFERENCES categories (id) ON DELETE CASCADE,
CONSTRAINT fk_users FOREIGN KEY (initiator_id) REFERENCES users (id) ON DELETE CASCADE,
CONSTRAINT fk_location FOREIGN KEY (location_id) REFERENCES locations (id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS requests (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
created TIMESTAMP NOT NULL,
event_id BIGINT NOT NULL,
requester_id BIGINT NOT NULL,
status VARCHAR NOT NULL,
CONSTRAINT fk_requester FOREIGN KEY (requester_id) REFERENCES users (id) ON DELETE CASCADE,
CONSTRAINT fk_event FOREIGN KEY (event_id) REFERENCES events (id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS compilations (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
pinned BOOLEAN DEFAULT FALSE,
title VARCHAR(50) NOT NULL
);

CREATE TABLE IF NOT EXISTS compilations_events
(
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
compilation_id BIGINT NOT NULL,
event_id BIGINT NOT NULL,
CONSTRAINT fk_compilation FOREIGN KEY (compilation_id) REFERENCES compilations (id),
CONSTRAINT fk_event    FOREIGN KEY (event_id) REFERENCES events (id)
);